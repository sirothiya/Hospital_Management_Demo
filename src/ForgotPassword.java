    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String ques = "",ans="";
try{        
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/hospital","root","tiger");
            Statement smt=con.createStatement();
            String sq="select security,answer from user where username = '"+usernametf.getText()+"';";
            ResultSet rs=smt.executeQuery(sq);
            if(rs.next())
            {
                ques=rs.getString("security");
                ans=rs.getString("answer");
            }
            jTextField3.setText(ques);
            String ans1 = jTextField1.getText().toLowerCase();
            if(ans1.equals(""))
                JOptionPane.showMessageDialog(null,"Enter Answer");
            else if(ans1.equals(ans))
            {
                JOptionPane.showMessageDialog(null,"Answer Matches");
                new ForgotPassword().setVisible(false);
                jDialog1.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Answer Does Not Matches");
                jTextField1.setText("");
            }
            rs.close();
            smt.close();
            con.close();
}
catch(Exception e)
{
    JOptionPane.showMessageDialog(null,"Error in Connectivity");
}        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

