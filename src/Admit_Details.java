    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
String patient_id="",admit_id="",name="",gender="",admit_date="",room_ward_no="",status="",discharge_date="";
try{
    Class.forName("java.sql.Driver");
    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/hospital","root","tiger");
    Statement smt=con.createStatement();
    String item = (String)searchcb.getSelectedItem();
    String sq="";
    if(item.equals("Search by Admit ID"))
        sq="select * from admit where admit_id = '"+searchtf.getText()+"';";
    else if(item.equals("Search by Name"))
        sq="select * from admit where name = '"+searchtf.getText()+"';";
    else if(item.equals("Search by Room/Ward No."))
        sq="select * from admit where room_ward_no = '"+searchtf.getText()+"';";
    ResultSet rs=smt.executeQuery(sq);
    model.setRowCount(0);
    while(rs.next())
    {
        admit_id=rs.getString("admit_id");
        patient_id=rs.getString("patient_id");
        name=rs.getString("name");
        gender=rs.getString("gender");
        admit_date=rs.getString("date_of_admit");
        room_ward_no=rs.getString("room_ward_no");
        status=rs.getString("status");
        discharge_date=rs.getString("date_of_discharge");
        model.addRow(new Object[]{admit_id,patient_id,name,admit_date,room_ward_no,status,discharge_date});
    }
    if(patient_id.equals(""))
    {
        JOptionPane.showMessageDialog(null,"Enter Valid Information");
        searchtf.setText("");
    }
    else
    {
        admitidlbl.setText(admit_id);
        patientidlbl.setText(patient_id);
        namelbl.setText(name);
        genderlbl.setText(gender);
        admitdatelbl.setText(admit_date);
        room_wardlbl.setText(room_ward_no);
        statuslbl.setText(status);
        dischargelbl.setText(discharge_date);
    }
    rs.close();
    smt.close();
    con.close();
    }
catch(Exception e){
    JOptionPane.showMessageDialog(null,"Error in Connection");
}        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

